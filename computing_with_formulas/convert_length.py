kilometers_to_campus = 27.84 # kilometers

kilometers_to_inches = kilometers_to_campus * 39370.1 # inches
kilometers_to_feet = kilometers_to_inches / 12
kilometers_to_yards = kilometers_to_feet / 3 
kilometers_to_miles = kilometers_to_yards /1760 

print(f' Distance to inches is {kilometers_to_inches:.2f} inches')
print(f' Distance to feet is {kilometers_to_feet:.2f} feet')
print(f' Distance to yards is {kilometers_to_yards:.2f} yards')
print(f' Distance to miles is {kilometers_to_miles:.2f} miles')



